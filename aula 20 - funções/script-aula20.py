def mensagem(msg):
    print('—' * 30)
    print(msg)
    print('—' * 30)


def soma(a, b):
    print(f'A = {a} e B = {b}')
    s = a + b
    print(f'A soma entre A + B = {s}')


#Programa principal
#Função mensagem()
mensagem('COPA LIBERTADORES')
mensagem('CAMPEONATO BRASILEIRO')
mensagem('COPA DO BRASIL')

#Função soma()
soma(a=4, b=5)
soma(8, 9)
soma(2, 1)

